//Códigos de repetição

let i = 0;

console.log("While: ");

while (i < 6) {
    console.log(`Passo ${i}`);
    i++;
}

i = 0;
console.log("Do while: ");

do {
    console.log(`Passo ${i}`);
    i++;
} while (i < 6);

console.log("Repetição do for: ")
for (let j = 0; j < 10; j++) {
    console.log(`Passo ${j}`)
}